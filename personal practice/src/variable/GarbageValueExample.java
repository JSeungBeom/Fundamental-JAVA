package variable;

public class GarbageValueExample {

	public static void main(String[] args) {
		byte var1 = 125;
		int var2 = 125; 
		// overflow가 발생하여, byte 타입 변수 리터럴이 127에서 1을 더하면 -128이 된다
		for(int i = 0; i < 5; i++) {
			var1++;
			var2++;
			System.out.println("var1: " + var1 + "\t" + "var2: " + var2	);
		}
	}

}
